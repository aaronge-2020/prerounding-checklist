import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = path.join(repoRoot, "medical-knowledge", "manifest.json");
const outputPath = path.join(repoRoot, "medical-knowledge-db.js");
const databaseSchemaVersion = "medical_knowledge_database_v1";
const placeholderExamTechniquePattern = /\bperform the named (?:bedside item|maneuver) directly\b/i;
const placeholderDiagnosticTargetPattern = /\bfocused bedside finding relevant to\b|^\s*$/i;
const basicBedsideDataPattern = /\b(?:measure|check|document|calculate|obtain|record)\s+(?:blood pressure|bp|heart rate|hr|respiratory rate|rr|oxygen saturation|spo2|pulse oximetry|temperature|temp|current weight|weight|body mass index|bmi|waist circumference|orthostatic|bedside glucose|point-of-care glucose|fingerstick glucose|pain score|mental status|intake|oral intake|fluid intake|urine output|wet napp(?:y|ies))\b|\b(?:assess|document|observe)\s+(?:general appearance|mental status|ability to protect airway|airway protection|intake and urine output|intake\/output)\b|\b(?:check|document|verify)\s+(?:ability to protect airway|airway protection)\b|^\s*mental status\s*$/i;
const bundledExamPattern = /[,;:]|\/|\band\s*\/\s*or\b|\b(?:focused exam|acuity screen|add repeat vitals|trigger exam|work-of-breathing|screen|assessment|vital signs including|cardiac exam|pulmonary exam|perfusion and pulses|volume status)\b/i;
const vagueExamActionPattern = /^(?:Check|Assess|Evaluate|Screen|Review|Document|Perform)\b/i;
const weakSafetyCheckLabelPattern = /^(?:Assess|Check|Evaluate)\b|^(?:Mental status|General appearance)$/i;
const staleGeneratedProvenancePattern = /\bgenerated endocrine workup\b|Generated from guideline-backed endocrine workup automation/i;
const sourceGovernanceCurrencyStatuses = new Set([
  "reviewed_current_for_scope",
  "reviewed_legacy_active",
  "review_due",
  "retired"
]);
const structuredApplicabilityModuleIds = new Set([
  "amenorrhea_v1",
  "erectile_dysfunction_v1",
  "gestational_diabetes_v1",
  "gynecomastia_v1",
  "hirsutism_v1",
  "hypogonadism_v1",
  "infertility_v1",
  "menopause_premature_ovarian_insufficiency_v1",
  "polycystic_ovary_syndrome_v1"
]);
const pregnancyStatusApplicabilityValues = new Set([
  "pregnant",
  "must_assess",
  "not_required_but_must_consider",
  "not_applicable"
]);

function readJson(relativeOrAbsolutePath) {
  const absolutePath = path.isAbsolute(relativeOrAbsolutePath)
    ? relativeOrAbsolutePath
    : path.join(repoRoot, relativeOrAbsolutePath);
  return JSON.parse(readFileSync(absolutePath, "utf8"));
}

function formatGeneratedModule({ manifest, sourceRegistry, complaintModules }) {
  const generated = {
    schema_version: databaseSchemaVersion,
    database_id: manifest.database_id,
    title: manifest.title,
    version: manifest.version,
    complaint_module_count: complaintModules.length,
    source_count: sourceRegistry.sources.length
  };

  return `// Generated by scripts/build-medical-knowledge-db.js. Do not edit by hand.\n`
    + `// Edit files under medical-knowledge/ and run npm run build:medical-knowledge.\n\n`
    + `export const medicalKnowledgeDbSchemaVersion = ${JSON.stringify(databaseSchemaVersion)};\n`
    + `export const medicalKnowledgeDbManifest = ${JSON.stringify(generated, null, 2)};\n\n`
    + `export const complaintSourceRegistry = ${JSON.stringify(sourceRegistry.sources, null, 2)};\n\n`
    + `export const complaintModules = ${JSON.stringify(complaintModules, null, 2)};\n`;
}

function itemSourceId(item) {
  return item?.source?.source_id || item?.source_id || "";
}

function collectModuleItems(module) {
  return [
    "redFlags",
    "safetyChecks",
    "requiredQuestions",
    "conditionalQuestions",
    "requiredExam",
    "conditionalExam",
    "initialTests",
    "dispositionRules",
    "decisionTrees",
    "treatmentOptions",
    "differentialBuckets"
  ].flatMap((group) => (module[group] || []).map((item) => ({ group, item })));
}

function mapModuleItemGroups(module, mapper) {
  const groups = [
    "redFlags",
    "safetyChecks",
    "requiredQuestions",
    "conditionalQuestions",
    "requiredExam",
    "conditionalExam",
    "initialTests",
    "dispositionRules",
    "decisionTrees",
    "treatmentOptions",
    "differentialBuckets"
  ];
  return groups.reduce((nextModule, group) => {
    if (!Array.isArray(nextModule[group])) {
      return nextModule;
    }
    return {
      ...nextModule,
      [group]: nextModule[group].map((item) => mapper(item, group, nextModule))
    };
  }, module);
}

function isQuestionGroup(group) {
  return group === "requiredQuestions" || group === "conditionalQuestions";
}

function isExamGroup(group) {
  return group === "requiredExam" || group === "conditionalExam";
}

function hasClinicalValue(value) {
  if (Array.isArray(value)) {
    return value.some((entry) => hasClinicalValue(entry));
  }
  if (typeof value === "string") {
    return value.trim().length > 0;
  }
  return Boolean(value);
}

function normalizeClinicalBuildText(value = "") {
  return String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function genericFindingsOptions(value = "") {
  const rawOptions = Array.isArray(value) ? value : String(value || "").split(/[;|/]+/);
  const tokens = rawOptions
    .map((option) => normalizeClinicalBuildText(option))
    .filter(Boolean);
  const genericTokens = tokens.filter((option) => /^(?:normal|abnormal|present|absent|normal absent|abnormal present|unable|unable to assess|not assessed)$/.test(option));
  if (tokens.length < 3 || genericTokens.length !== tokens.length) {
    return false;
  }
  const joined = tokens.join(" ");
  return /\b(?:unable|unable to assess|not assessed)\b/.test(joined)
    && ((/\bnormal\b/.test(joined) && /\babnormal\b/.test(joined))
      || (/\bpresent\b/.test(joined) && /\babsent\b/.test(joined)));
}

function requireItemFields(issues, moduleId, group, item, fields, label = "") {
  for (const field of fields) {
    if (!hasClinicalValue(item[field])) {
      issues.push(`${moduleId}.${group}.${item.id || "missing"} missing ${label || field}${label ? ` (${field})` : ""}`);
    }
  }
}

const validLrPattern = /^(?:n\/a|na|not available|not studied|pending|unavailable|[<>]?\s*\d+(?:\.\d+)?(?:\s*-\s*[<>]?\s*\d+(?:\.\d+)?)?)$/i;
const validDifficultyValues = new Set(["easy", "moderate", "hard", "difficult"]);
const validCooperationValues = new Set(["low", "moderate", "high", "n/a"]);

function validateItemMetadataValues(issues, moduleId, group, item = {}) {
  ["LR_plus", "LR_minus"].forEach((field) => {
    const value = item[field];
    if (hasClinicalValue(value) && !validLrPattern.test(String(value).trim())) {
      issues.push(`${moduleId}.${group}.${item.id || "missing"} invalid ${field}: ${value}`);
    }
  });

  if (group === "safetyChecks" || isExamGroup(group)) {
    const minutes = Number(item.time_burden_minutes);
    if (!Number.isFinite(minutes) || minutes < 0 || minutes > 30) {
      issues.push(`${moduleId}.${group}.${item.id || "missing"} invalid time_burden_minutes: ${item.time_burden_minutes}`);
    }
    const difficulty = String(item.difficulty || "").trim().toLowerCase();
    if (difficulty && !validDifficultyValues.has(difficulty)) {
      issues.push(`${moduleId}.${group}.${item.id || "missing"} invalid difficulty: ${item.difficulty}`);
    }
    const cooperation = String(item.patient_cooperation_required || "").trim().toLowerCase();
    if (cooperation && !validCooperationValues.has(cooperation)) {
      issues.push(`${moduleId}.${group}.${item.id || "missing"} invalid patient_cooperation_required: ${item.patient_cooperation_required}`);
    }
  }
}

function expectedItemTypeForGroup(group = "") {
  if (group === "safetyChecks") return "safety_check";
  if (isQuestionGroup(group)) return "history_question";
  if (isExamGroup(group)) return "physical_exam_maneuver";
  if (group === "redFlags") return "red_flag";
  if (group === "initialTests") return "diagnostic_test";
  if (group === "dispositionRules") return "management_change";
  if (group === "decisionTrees" || group === "treatmentOptions") return "management_change";
  if (group === "differentialBuckets") return "diagnostic_frame";
  return "";
}

function isBasicBedsideDataItem(item = {}) {
  const text = `${item.id || ""} ${item.label || ""} ${item.action || ""}`;
  return basicBedsideDataPattern.test(text)
    || /(?:_measure_bp|_measure_hr|_measure_rr|_measure_spo2|_measure_temperature|_measure_weight|orthostatic|bedside_glucose|fingerstick|pain_score|mental_status|assess_mental_status|airway_protection)/i.test(text);
}

function expectedBasicBedsideEquipment(item = {}) {
  const text = `${item.id || ""} ${item.label || ""}`.toLowerCase();
  if (/\bblood pressure|\bbp\b|orthostatic|standing blood pressure/.test(text)) return "blood pressure cuff";
  if (/\bheart rate\b|\bhr\b|pulse rate/.test(text)) return "watch/timer or bedside monitor";
  if (/\brespiratory rate\b|\brr\b/.test(text)) return "watch/timer or bedside monitor";
  if (/oxygen saturation|spo2|pulse oximetry/.test(text)) return "pulse oximeter";
  if (/temperature|temp/.test(text)) return "thermometer";
  if (/waist circumference/.test(text)) return "tape measure";
  if (/body mass index|bmi/.test(text)) return "scale and height measurement";
  if (/current weight|\bweight\b/.test(text)) return "scale";
  if (/glucose|fingerstick/.test(text)) return "glucometer";
  if (/general appearance|mental status|pain score/.test(text)) return "none";
  return "";
}

function safetyEquipmentIssue(item = {}) {
  const expected = expectedBasicBedsideEquipment(item);
  if (!expected) return "";
  const actual = String(item.equipment_needed || "").trim().toLowerCase();
  const normalizedExpected = expected.toLowerCase();
  if (!actual) return `expected ${expected}`;
  if (normalizedExpected === "none") {
    return actual === "none" ? "" : `expected ${expected}, got ${item.equipment_needed}`;
  }
  const expectedAny = normalizedExpected.split(/\s+or\s+|,\s*/).map((value) => value.trim()).filter(Boolean);
  const matches = expectedAny.some((option) => actual.includes(option)) || actual.includes(normalizedExpected);
  return matches ? "" : `expected ${expected}, got ${item.equipment_needed}`;
}

function questionLikelihoodRatioNote(item = {}) {
  const existing = item.likelihood_ratio_note || item.LR_note || item.lr_note || item.likelihoodRatioNote;
  if (String(existing || "").trim()) {
    return existing;
  }
  return "Question-level LR+/LR- is not available unless the cited evidence validates the exact response; use this answer to localize the source, assess severity, and guide management.";
}

function cleanSupportLabel(value = "") {
  return String(value || "")
    .replace(/\s+/g, " ")
    .replace(/[.;\s]+$/g, "")
    .trim();
}

function supportLabelList(items = [], max = 3) {
  return items
    .map((item) => cleanSupportLabel(item?.label || item?.action || item?.management_change || item?.text || ""))
    .filter(Boolean)
    .slice(0, max)
    .join("; ");
}

function supportTags(module = {}, label = "") {
  return `${module.id || ""} ${module.label || ""} ${label}`
    .toLowerCase()
    .replace(/[()]/g, " ")
    .split(/[^a-z0-9+]+/)
    .filter((term) => term.length >= 4)
    .slice(0, 14);
}

function sourceForDerivedSupport(module = {}, preferredItem = {}, section = "") {
  const fallbackItem = preferredItem?.source
    ? preferredItem
    : [
      ...(module.initialTests || []),
      ...(module.dispositionRules || []),
      ...(module.redFlags || []),
      ...(module.differentialBuckets || [])
    ].find((item) => item?.source);
  const source = fallbackItem?.source || {};
  return {
    ...source,
    source_section: section || source.source_section || "derived source-backed decision support"
  };
}

function derivedSupportItem(module = {}, id = "", label = "", sourceItem = {}, section = "", rationale = "") {
  const source = sourceForDerivedSupport(module, sourceItem, section);
  return {
    id,
    label,
    item_type: "management_change",
    source,
    action: label,
    rationale: rationale || `Derives a ${module.label || "selected"} workup decision from existing source-backed tests, red flags, and management rules.`,
    diagnostic_target: `${label}: result or bedside context that changes testing, treatment, monitoring, referral, or disposition for ${module.label || "the selected workup"}.`,
    management_change: label,
    LR_plus: "n/a",
    LR_minus: "n/a",
    likelihood_ratio_note: "Likelihood ratios are not applicable to this decision-support row; use it to route source-backed testing, treatment, monitoring, referral, or disposition.",
    limitations: `Use with clinician judgment, local protocols, patient-specific risks, and reassessment; this derived row summarizes existing source-backed module content for ${module.label || "the selected workup"}.`,
    tags: supportTags(module, label)
  };
}

function deriveDecisionTrees(module = {}) {
  const tests = module.initialTests || [];
  const redFlags = module.redFlags || [];
  const dispositions = module.dispositionRules || [];
  const differentials = module.differentialBuckets || [];
  const escalation = cleanSupportLabel(redFlags[0]?.label || dispositions[0]?.label || "unstable or high-risk presentation");
  const testList = supportLabelList(tests, 4) || "use the source-backed initial test set";
  const riskFrame = cleanSupportLabel(differentials[0]?.label || tests[0]?.label || "the highest-risk diagnostic frame");
  const management = cleanSupportLabel(dispositions[0]?.label || redFlags[0]?.action || "the source-backed escalation route");
  return [
    derivedSupportItem(module, "decision_01", `Screen for immediate danger or disposition-changing findings before routine workup: ${escalation}.`, redFlags[0] || dispositions[0] || tests[0], `${module.label} derived decision tree: triage and escalation`),
    derivedSupportItem(module, "decision_02", `Order focused first-line studies and interpret them in sequence: ${testList}.`, tests[0] || dispositions[0], `${module.label} derived decision tree: initial tests`),
    derivedSupportItem(module, "decision_03", `Use the leading diagnostic frame to avoid premature closure: ${riskFrame}.`, differentials[0] || tests[0], `${module.label} derived decision tree: diagnostic branching`),
    derivedSupportItem(module, "decision_04", `If results or bedside status are high-risk, abnormal, worsening, or discordant, follow the source-backed management route: ${management}.`, dispositions[0] || redFlags[0] || tests[0], `${module.label} derived decision tree: management routing`)
  ];
}

function deriveTreatmentOptions(module = {}) {
  const tests = module.initialTests || [];
  const redFlags = module.redFlags || [];
  const dispositions = module.dispositionRules || [];
  const firstDisposition = cleanSupportLabel(dispositions[0]?.label || redFlags[0]?.action || "urgent escalation route");
  const secondDisposition = cleanSupportLabel(dispositions[1]?.label || dispositions[0]?.action || "lower-risk treatment or follow-up route when criteria are met");
  const monitoring = cleanSupportLabel(tests[0]?.label || "the source-backed monitoring test set");
  const safety = cleanSupportLabel(redFlags[0]?.label || "clinical worsening or unresolved diagnostic uncertainty");
  return [
    derivedSupportItem(module, "treatment_01", `Stabilize or escalate before routine treatment when danger criteria are present: ${safety}.`, redFlags[0] || dispositions[0] || tests[0], `${module.label} derived treatment options: urgent care`),
    derivedSupportItem(module, "treatment_02", `Use the high-risk or confirmed-pathway management option when criteria are met: ${firstDisposition}.`, dispositions[0] || redFlags[0] || tests[0], `${module.label} derived treatment options: high-risk pathway`),
    derivedSupportItem(module, "treatment_03", `Use the lower-risk, outpatient, supportive, or safety-net pathway only when the source-backed criteria are satisfied: ${secondDisposition}.`, dispositions[1] || dispositions[0] || tests[0], `${module.label} derived treatment options: lower-risk pathway`),
    derivedSupportItem(module, "treatment_04", `Monitor treatment response, complications, and need for reassessment with: ${monitoring}.`, tests[0] || dispositions[0], `${module.label} derived treatment options: monitoring`)
  ];
}

function deriveDecisionSupportForBuild(module = {}) {
  const decisionTrees = Array.isArray(module.decisionTrees) && module.decisionTrees.length
    ? module.decisionTrees
    : deriveDecisionTrees(module);
  const treatmentOptions = Array.isArray(module.treatmentOptions) && module.treatmentOptions.length
    ? module.treatmentOptions
    : deriveTreatmentOptions(module);
  return {
    ...module,
    decisionTrees,
    treatmentOptions
  };
}

function normalizeMedicalKnowledgeModuleForBuild(module = {}) {
  const normalized = mapModuleItemGroups(module, (item, group) => {
    if (!isQuestionGroup(group)) {
      return item;
    }
    return {
      ...item,
      likelihood_ratio_note: questionLikelihoodRatioNote(item)
    };
  });
  return deriveDecisionSupportForBuild(normalized);
}

export function loadMedicalKnowledgeDatabase() {
  if (!existsSync(manifestPath)) {
    throw new Error("medical-knowledge/manifest.json is missing.");
  }
  const manifest = readJson(manifestPath);
  const sourceRegistry = readJson(manifest.source_registry || "medical-knowledge/source-registry.json");
  const complaintModules = (manifest.complaint_modules || []).map((modulePath) => {
    const envelope = readJson(modulePath);
    return normalizeMedicalKnowledgeModuleForBuild(envelope.module || envelope);
  });

  return { manifest, sourceRegistry, complaintModules };
}

export function validateMedicalKnowledgeDatabase(database = loadMedicalKnowledgeDatabase()) {
  const issues = [];
  const { manifest, sourceRegistry, complaintModules } = database;

  if (manifest.schema_version !== databaseSchemaVersion) {
    issues.push(`manifest.schema_version must be ${databaseSchemaVersion}`);
  }
  if (!manifest.database_id || !manifest.title || !manifest.version) {
    issues.push("manifest must include database_id, title, and version");
  }
  if (sourceRegistry.schema_version !== databaseSchemaVersion) {
    issues.push(`source-registry schema_version must be ${databaseSchemaVersion}`);
  }
  if (!Array.isArray(sourceRegistry.sources) || !sourceRegistry.sources.length) {
    issues.push("source registry must contain at least one source");
  }

  const sourceIds = new Set();
  for (const source of sourceRegistry.sources || []) {
    if (!source.id) {
      issues.push("source registry row is missing id");
      continue;
    }
    if (sourceIds.has(source.id)) {
      issues.push(`duplicate source id ${source.id}`);
    }
    sourceIds.add(source.id);
    for (const field of ["title", "source", "version", "url", "date_accessed", "citation"]) {
      if (!source[field]) {
        issues.push(`${source.id} is missing ${field}`);
      }
    }
    for (const field of ["review_owner", "reviewed_by_role", "last_reviewed", "next_review_due", "currency_status"]) {
      if (!source[field]) {
        issues.push(`${source.id} is missing source governance field ${field}`);
      }
    }
    for (const field of ["date_accessed", "last_reviewed", "next_review_due"]) {
      if (source[field] && !/^\d{4}-\d{2}-\d{2}$/.test(String(source[field]))) {
        issues.push(`${source.id} source ${field} must use YYYY-MM-DD`);
      }
    }
    if (source.last_reviewed && source.date_accessed && source.last_reviewed < source.date_accessed) {
      issues.push(`${source.id} source last_reviewed must be on or after date_accessed`);
    }
    if (source.next_review_due && source.last_reviewed && source.next_review_due <= source.last_reviewed) {
      issues.push(`${source.id} source next_review_due must be later than last_reviewed`);
    }
    if (source.currency_status && !sourceGovernanceCurrencyStatuses.has(source.currency_status)) {
      issues.push(`${source.id} source currency_status must be one of ${Array.from(sourceGovernanceCurrencyStatuses).join(", ")}`);
    }
  }

  const moduleIds = new Set();
  for (const module of complaintModules) {
    if (!module.id) {
      issues.push("complaint module is missing id");
      continue;
    }
    if (moduleIds.has(module.id)) {
      issues.push(`duplicate complaint module id ${module.id}`);
    }
    moduleIds.add(module.id);
    for (const field of ["schema_version", "label", "version", "status", "triggers"]) {
      if (!module[field] || (Array.isArray(module[field]) && !module[field].length)) {
        issues.push(`${module.id} is missing ${field}`);
      }
    }
    if (structuredApplicabilityModuleIds.has(module.id)) {
      const applicability = module.applicability || {};
      for (const field of ["sex_or_reproductive_context", "pregnancy_status_required", "review_owner", "last_reviewed"]) {
        if (!applicability[field]) {
          issues.push(`${module.id} applicability missing ${field}`);
        }
      }
      if (!pregnancyStatusApplicabilityValues.has(applicability.pregnancy_status_required)) {
        issues.push(`${module.id} applicability pregnancy_status_required must be one of ${Array.from(pregnancyStatusApplicabilityValues).join(", ")}`);
      }
      for (const field of ["use_when", "do_not_use_when"]) {
        if (!Array.isArray(applicability[field]) || !applicability[field].length) {
          issues.push(`${module.id} applicability missing ${field}`);
        }
      }
      if (applicability.last_reviewed && !/^\d{4}-\d{2}-\d{2}$/.test(String(applicability.last_reviewed))) {
        issues.push(`${module.id} applicability last_reviewed must use YYYY-MM-DD`);
      }
      if (module.id === "gestational_diabetes_v1" && applicability.pregnancy_status_required !== "pregnant") {
        issues.push("gestational_diabetes_v1 applicability must require active pregnancy");
      }
    }
    for (const { group, item } of collectModuleItems(module)) {
      if (!item.id || !item.label) {
        issues.push(`${module.id}.${group} has an item missing id or label`);
      }
      const expectedItemType = expectedItemTypeForGroup(group);
      if (expectedItemType && item.item_type !== expectedItemType) {
        issues.push(`${module.id}.${group}.${item.id || "missing"} item_type must be ${expectedItemType}, got ${item.item_type || "missing"}`);
      }
      const sourceId = itemSourceId(item);
      if (!sourceId || !sourceIds.has(sourceId)) {
        issues.push(`${module.id}.${group}.${item.id || "missing"} references invalid source ${sourceId || "missing"}`);
      }
      for (const field of ["source_section", "version_date", "last_reviewed", "clinical_owner"]) {
        if (!item.source?.[field]) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} source missing ${field}`);
        }
      }
      if (staleGeneratedProvenancePattern.test(`${item.source?.source_section || ""} ${item.source?.implementation_notes || ""}`)) {
        issues.push(`${module.id}.${group}.${item.id || "missing"} source provenance uses stale generated-workup wording`);
      }
      validateItemMetadataValues(issues, module.id, group, item);
      if (isQuestionGroup(group)) {
        requireItemFields(issues, module.id, group, item, ["text", "when_to_ask", "diagnostic_purpose", "management_implication", "likelihood_ratio_note"]);
        if (!item.options?.length) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} missing answer options`);
        }
        if (!item.tags?.length) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} missing tags`);
        }
        if (!/\?$/.test(String(item.text || item.label || "").trim())) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} must be phrased as an askable question`);
        }
        if (/\bAny .+\brelevant to\b/i.test(`${item.text || ""} ${item.label || ""}`)) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} uses generic relevance boilerplate instead of a clinically specific question`);
        }
      }
      if (isExamGroup(group)) {
        if (isBasicBedsideDataItem(item)) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} basic bedside data/safety item belongs in safetyChecks, not physical exam: ${item.label}`);
        }
        if (bundledExamPattern.test(item.label || "")) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} exam label appears bundled or vague: ${item.label}`);
        }
        if (vagueExamActionPattern.test(item.label || "")) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} exam label must name a concrete maneuver action: ${item.label}`);
        }
        if (genericFindingsOptions(item.findings_options || item.findingsOptions || item.options)) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} findings_options are generic; use maneuver-specific documentation choices`);
        }
        for (const field of ["technique", "findings_options", "when_to_perform", "diagnostic_target", "LR_plus", "LR_minus", "likelihood_ratio_note", "management_change", "difficulty", "time_burden_minutes", "equipment_needed", "patient_cooperation_required", "limitations", "tags"]) {
          if (!hasClinicalValue(item[field])) {
            issues.push(`${module.id}.${group}.${item.id || "missing"} missing ${field}`);
          }
        }
        if (placeholderExamTechniquePattern.test(item.technique || "")) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} has placeholder technique`);
        }
        if (placeholderDiagnosticTargetPattern.test(item.diagnostic_target || "")) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} has generic diagnostic_target`);
        }
        if (/\bDocuments focused endocrine signs and complications that help distinguish severity, mimics, and management priorities\b/i.test(item.rationale || "")) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} has generic exam rationale`);
        }
      }
      if (group === "redFlags") {
        requireItemFields(issues, module.id, group, item, ["action", "rationale", "diagnostic_target", "management_change", "LR_plus", "LR_minus", "likelihood_ratio_note", "limitations", "tags"]);
      }
      if (group === "safetyChecks") {
        if (weakSafetyCheckLabelPattern.test(item.label || "")) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} safety-check label should name a specific bedside action: ${item.label}`);
        }
        requireItemFields(
          issues,
          module.id,
          group,
          item,
          [
            "action",
            "rationale",
            "management_change",
            "difficulty",
            "time_burden_minutes",
            "equipment_needed",
            "patient_cooperation_required",
            "limitations",
            "likelihood_ratio_note",
            "tags"
          ]
        );
        const equipmentIssue = safetyEquipmentIssue(item);
        if (equipmentIssue) {
          issues.push(`${module.id}.${group}.${item.id || "missing"} safety-check equipment mismatch: ${equipmentIssue}`);
        }
      }
      if (group === "initialTests") {
        requireItemFields(issues, module.id, group, item, ["action", "rationale", "diagnostic_target", "management_change", "LR_plus", "LR_minus", "likelihood_ratio_note", "limitations", "tags"]);
      }
      if (group === "dispositionRules" || group === "decisionTrees" || group === "treatmentOptions") {
        requireItemFields(issues, module.id, group, item, ["action", "rationale", "diagnostic_target", "management_change", "LR_plus", "LR_minus", "likelihood_ratio_note", "limitations", "tags"]);
      }
      if (group === "differentialBuckets") {
        requireItemFields(issues, module.id, group, item, ["action", "rationale", "diagnostic_target", "management_change", "LR_plus", "LR_minus", "likelihood_ratio_note", "limitations", "tags"]);
      }
    }
  }

  return { ok: issues.length === 0, issues, sourceCount: sourceIds.size, moduleCount: moduleIds.size };
}

export function buildMedicalKnowledgeDatabase(options = {}) {
  const database = loadMedicalKnowledgeDatabase();
  const validation = validateMedicalKnowledgeDatabase(database);
  if (!validation.ok) {
    throw new Error(validation.issues.join("\n"));
  }
  const generated = formatGeneratedModule(database);
  if (options.check) {
    const current = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
    if (current !== generated) {
      throw new Error("medical-knowledge-db.js is out of date. Run npm run build:medical-knowledge.");
    }
    return { validation, wrote: false };
  }
  writeFileSync(outputPath, generated);
  return { validation, wrote: true };
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const check = process.argv.includes("--check");
  try {
    const result = buildMedicalKnowledgeDatabase({ check });
    const action = result.wrote ? "Built" : "Verified";
    console.log(`${action} medical knowledge database: ${result.validation.moduleCount} modules, ${result.validation.sourceCount} sources.`);
  } catch (error) {
    console.error(error?.message || error);
    process.exitCode = 1;
  }
}
