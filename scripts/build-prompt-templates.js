import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const templatesDir = path.join(repoRoot, "prompts", "templates");
const outputPath = path.join(repoRoot, "prompts", "templates-bundle.js");

const files = readdirSync(templatesDir)
  .filter((f) => f.endsWith(".md"))
  .sort();

if (!files.length) {
  console.error("No .md template files found in", templatesDir);
  process.exit(1);
}

let output = "// Generated by scripts/build-prompt-templates.js — do not hand-edit.\n\n";

for (const file of files) {
  const stem = file.replace(/\.md$/, "");
  const exportName = stem.replace(/[^a-zA-Z0-9_]/g, "_");
  const content = readFileSync(path.join(templatesDir, file), "utf-8");
  const escaped = content
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$/g, "\\$");
  output += `export const ${exportName} = \`${escaped}\`;\n`;
}

writeFileSync(outputPath, output, "utf-8");

console.log(`Wrote ${files.length} templates to ${path.relative(repoRoot, outputPath)}`);
for (const file of files) {
  const exportName = file.replace(/\.md$/, "").replace(/[^a-zA-Z0-9_]/g, "_");
  console.log(`  ${exportName} <- ${file}`);
}
